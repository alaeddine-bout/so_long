#include"libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*p;

	i = ft_strlen(s1);
	p = malloc(sizeof(char) * (i + 1));
	if (!p)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
